/**
 * Operadores lógicos
 * 
 * && -> AND -> E
 * || -> OR -> OU
 * ! -> NOT -> NÃO
 */

console.log(true && true && true && true); //para que o retorno seja true, é necessario que todas as expressões sejam verdadeiras
console.log(true || false || false || false); //se qualquer uma das expressões forem true, o retorno será true
console.log(!true); //inverte a expressão

